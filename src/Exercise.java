public class Exercise {
    private String name;
    private String link;

    public Exercise(String name, String link) {
        this.name = name;
        this.link = link;
    }

    public String getName() {
        return name;
    }

    public String getLink() {
        return link;
    }

    @Override
    public String toString() {
        return name;
    }
}